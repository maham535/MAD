// Valid: statement ends with a semicolon
var sum = 10 + 5;  // Preferred way with semicolon

// Valid without a semicolon (but not recommended)
var diff = 20 - 5  // No semicolon, still works due to JavaScript's automatic semicolon insertion

// Valid: statement ends with a semicolon
var product = 6 * 3;  // Preferred way with semicolon

// Another statement without semicolon (works but not recommended)
var quotient = 30 / 5  // No semicolon, still works

console.log(sum);      // Output: 15
console.log(diff);     // Output: 15
console.log(product);  // Output: 18
console.log(quotient); // Output: 6
